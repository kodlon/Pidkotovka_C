/*Користувач вводить числа m та n і друкує прямокутний
візерунок розміру m * n. Тобто в i-му рядку в j-й позиції
має стояти або символ "[", якщо i + j парне число, або
символ "]" в іншому випадку.*/

#include <stdio.h>

int main(){
  int m, n;

  scanf("%d %d", &m, &n);

  for ( int i = 0; i < m; i++){
    for (int k = 0; k < n; k++){
      if( (i + k) % 2 == 0) printf(" [");
      else printf("] ");
    }
    printf("\n");
  }
  return 0;
}
