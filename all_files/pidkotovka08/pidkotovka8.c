/*Програма вводить цілі числа m та n
потім перебирає по порядку всі числа k
у проміжку від m до n і друкує на екран ті
з них, для яких k^2 + 2k + 7 ділиться на 3. */

#include <stdio.h>

int main() {
  int m, n, k;

  scanf("%d %d", &m, &n);
  for (int i = m; i <= n; i++) {
    k = i * i + 2 * i + 7;
    if (k % 3 == 0) printf("%d %d\n", k, i);
  }
  return 0;
}
